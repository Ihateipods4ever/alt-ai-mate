#!/bin/bash

echo "🔧 Fixing Build Issues for ALT-AI-MATE"
echo "======================================"

# Navigate to client directory
cd packages/client

# Check if there are any missing UI components
echo "📋 Checking for missing UI components..."

# Create any missing UI components that might be referenced
if [ ! -f "src/components/ui/separator.tsx" ]; then
    echo "📝 Creating missing separator component..."
    cat > src/components/ui/separator.tsx << 'EOF'
import * as React from "react"
import * as SeparatorPrimitive from "@radix-ui/react-separator"

import { cn } from "@/lib/utils"

const Separator = React.forwardRef<
  React.ElementRef<typeof SeparatorPrimitive.Root>,
  React.ComponentPropsWithoutRef<typeof SeparatorPrimitive.Root>
>(
  (
    { className, orientation = "horizontal", decorative = true, ...props },
    ref
  ) => (
    <SeparatorPrimitive.Root
      ref={ref}
      decorative={decorative}
      orientation={orientation}
      className={cn(
        "shrink-0 bg-border",
        orientation === "horizontal" ? "h-[1px] w-full" : "h-full w-[1px]",
        className
      )}
      {...props}
    />
  )
)
Separator.displayName = SeparatorPrimitive.Root.displayName

export { Separator }
EOF
fi

# Check for any TypeScript errors in our new files
echo "🔍 Checking TypeScript files..."

# Fix any potential import issues
echo "🔧 Fixing potential import issues..."

# Update the main.tsx to ensure proper imports
if [ -f "src/main.tsx" ]; then
    echo "✅ main.tsx exists"
else
    echo "❌ main.tsx missing - creating it..."
    cat > src/main.tsx << 'EOF'
import React from 'react'
import ReactDOM from 'react-dom/client'
import App from './App.tsx'
import './index.css'

ReactDOM.createRoot(document.getElementById('root')!).render(
  <React.StrictMode>
    <App />
  </React.StrictMode>,
)
EOF
fi

# Ensure index.css exists
if [ ! -f "src/index.css" ]; then
    echo "📝 Creating missing index.css..."
    cat > src/index.css << 'EOF'
